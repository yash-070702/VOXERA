const UserElement = ({ img, firstName, lastName, online, _id }) => {
  const theme = useTheme();
 const {user} = useSelector((state)=>state.profile);
 const {sendFriendRequests}=useSelector((state)=>state.profile);
 const {friends}=useSelector((state)=>state.profile);
 console.log(friends);
 const user_id=user._id;

  const name = `${firstName} ${lastName}`;

  return (
    <StyledChatBox
      sx={{
        width: "100%",

        borderRadius: 1,

        backgroundColor: theme.palette.background.paper,
      }}
      p={2}
    >
      <Stack
        direction="row"
        alignItems={"center"}
        justifyContent="space-between"
      >
        <Stack direction="row" alignItems={"center"} spacing={2}>
          {" "}
          {online ? (
            <StyledBadge
              overlap="circular"
              anchorOrigin={{ vertical: "bottom", horizontal: "right" }}
              variant="dot"
            >
              <Avatar alt={name} src={img} />
            </StyledBadge>
          ) : (
            <Avatar alt={name} src={img} />
          )}
          <Stack spacing={0.3}>
            <Typography variant="subtitle2">{name}</Typography>
          </Stack>
        </Stack>
        <Stack direction={"row"} spacing={2} alignItems={"center"}>
        {
          sendFriendRequests.find(item => item.recipient._id === _id) &&
          <Button
          
          >
            Cancel Request
          </Button>
        }
        {
          friends.find(item => item._id === _id)&&
          <Button
           
          >
          Delete Friend
          </Button>
        }
        {
          !friends.find(item => item._id === _id) && !sendFriendRequests.find(item => item.recipient._id === _id)&&
          <Button
            onClick={() => {
              socket.emit("friend_request", { to: _id, from: user_id }, () => {
                alert("request sent");
              });
            }}
          >
            Send Request
          </Button>
        }
        </Stack>
      </Stack>
    </StyledChatBox>
  );
};

